import Foundation

extension UIColor {
    
    static func deepRed() -> UIColor {
        return UIColor(
            red: 178.0/255.0,
            green: 28.0/255.0,
            blue: 78.0/255.0,
            alpha: 1.0)
    }
    
    static func intenseBlue() -> UIColor {
        return UIColor(
            red: 77.0/255.0,
            green: 108.0/255.0,
            blue: 250.0/255.0,
            alpha: 1.0)
    }
    
}